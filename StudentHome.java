    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String roll=jTextField1.getText();
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Password@123");
            Statement stmt=conn.createStatement();
            ResultSet rs=stmt.executeQuery("select * from students inner join result where students.roll='"+roll+"' AND result.roll='"+roll+"'");
            if(rs.next()) {
                setVisible(false);
                new StudentPortal(roll).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null,"Incorrect Roll Number");
            }
        }catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

