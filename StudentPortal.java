    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        try{
            String roll=(String)jTextField3.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root", "Password@123");
            Statement stmt=conn.createStatement();
            ResultSet rs=stmt.executeQuery("select * from students inner join result where students.roll='"+roll+"' AND result.roll='"+roll+"' ");
            if(rs.next()) {
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(rs.getString(1));
                jTextField6.setText(rs.getString(6));
                
                jTextField9.setText(rs.getString(8));
                jTextField12.setText(rs.getString(9));
                jTextField15.setText(rs.getString(10));
                jTextField18.setText(rs.getString(11));
                jTextField21.setText(rs.getString(12));
                
                int s1=Integer.parseInt(jTextField9.getText());
                int s2=Integer.parseInt(jTextField12.getText());
                int s3=Integer.parseInt(jTextField15.getText());
                int s4=Integer.parseInt(jTextField18.getText());
                int s5=Integer.parseInt(jTextField21.getText());
                
                int result=s1+s2+s3+s4+s5;
                
                jTextField22.setText(String.valueOf(result));
                
                if(result>=30) {
                    jTextField23.setText("pass");
                } else {
                    jTextField23.setText("fail");
                }
                    
                     
                
            }else {
                JOptionPane.showMessageDialog(null, "Invalid Roll Number");
            }
            
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_formComponentShown

